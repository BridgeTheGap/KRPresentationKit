import UIKit

public enum Attribute {
    
    case alpha(CGFloat)
    
    case frame(CGRect)
    
    case position(CGPoint)
    
    case opacity(Float)
    
    case origin(CGPoint)
    
    case rotation(CGFloat)
    
    case scale(CGFloat)
    
    case size(CGSize)
    
    case translation(CGSize)
    
}
